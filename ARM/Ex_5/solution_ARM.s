; Reset Handler

Reset_Handler   PROC
                EXPORT  Reset_Handler             [WEAK]
                ;IMPORT  SystemInit
                ;IMPORT  __main
                
				LDR r1, =constant
				MOV r2, r1
				
				BL KaprekarRoutine
				
                ENDP

KaprekarRoutine PROC
	            MOV r3, #0 ; contor
				MOV r6, #10 ; the divider
				LDR r8, =digits
division_loop
				CMP r3, #4
				BEQ descending_sort
				
				SDIV r4, r2, r6
				MUL r7, r4, r6
				SUB r5, r2, r7
				
				STRB r5, [r8, r3]
				MOV r2, r4
				;ADD r8, r8, #1
				ADD r3, r3, #1
				B division_loop
				
descending_sort 
				LDRB r3, [r8, #0]
				LDRB r4, [r8, #1]
				LDRB r5, [r8, #2]
				LDRB r6, [r8, #3]
				
				MOV r1, #1
				
loop            
				; If we have 4 numbers we need at least 3 iterations to sort the numbers, so n-1
				; We save the numbers from biggest to lowest at r3,r4,r5,r6
				CMP r1, #3
				BGT get_biggest_number
				
				CMP r3, r4
				ITTT LT
				MOVLT r9, r3
				MOVLT r3, r4
				MOVLT r4, r9
				
				CMP r4, r5
				ITTT LT
				MOVLT r9, r4
				MOVLT r4, r5
				MOVLT r5, r9
				
				CMP r5, r6
				ITTT LT
				MOVLT r9, r5
				MOVLT r5, r6
				MOVLT r6, r9
				
				ADD r1, r1, #1
				B loop
				
				
				B get_biggest_number
				
get_biggest_number
				; Lets say we have 5,3,2,0 we do 5*10 = 50 , 50+3 = 53, 53*10 = 530, 530+2 = 532, 532*10 = 5320,
				; 5320+0 = 5320
				mov r7, #10
				MUL r8, r3, r7
				ADD	r8, r8, r4
				MUL r8, r8, r7
				ADD r8, r8, r5
				MUL r8, r8, r7
				ADD r8, r8, r6
				B get_lowest_value

get_lowest_value 
				; The same thing as up, just we start with the smalles number which is 0 and is saved in r6
			    MUL r9, r6, r7
				ADD	r9, r9, r5
				MUL r9, r9, r7
				ADD r9, r9, r4
				MUL r9, r9, r7
				ADD r9, r9, r3
				B make_difference

make_difference
				SUB r8, r8, r9
				MOV r0, r8
				BX LR
				
				ENDP

